
public class banking {
	String bName;
	String cNumber;
	String expDate;
	
	public void setbName(String bbname)
	{
		bName = bbname;
	}
	
	public void setcNumber(String num)
	{
		cNumber = num;
	}
	
	public void setexpDate(String exp)
	{
		expDate = exp;
	}

	public String getbName()
	{
		return bName;
	}
	
	public String getcNumber()
	{
		return cNumber;
	}
	
	public String getexpDate()
	{
		return expDate;
	}
}
